    public static void asyncDot(double[] x, double[] y, int sectionSize){
        Scheduler sch = new Scheduler(8);
        ArrayList<Future<Double>> futureArray = new ArrayList<>();

        int cur = 0;
        Double total = 0.0;
        while(cur < x.length){
            final int c = cur;
            futureArray.add(sch.schedule(() -> {
                return dotProduct(x, y, c, Math.min(x.length, c + sectionSize));
            }));
            cur += sectionSize;
        }

        for(Future<Double> i : futureArray){
            total += i.get();
        }

        System.out.println(total);
    }

    public static double dotProduct(double[] x, double[] y, int start, int end){
        double res = 0.0;
        for (int i = start; i < end; i++) {
            // res += x[i] * y[i];
            res += Math.sin(Math.sqrt(Math.abs(x[i] * y[i])));
        }
        return res;
    }

